public class MathProblems {
    public static void main(String[] args) {
        // Example problem 1: Find the area of a rectangle
        double length = 5.0;
        double width = 3.0;
        double area = length * width;
        System.out.println("The area of the rectangle is: " + area);

        // Example problem 2: Calculate the perimeter of an equilateral triangle
        double sideLength = 4.0;
        double perimeter = 3 * sideLength;
        System.out.println("The perimeter of the equilateral triangle is: " + perimeter);
    }
}
