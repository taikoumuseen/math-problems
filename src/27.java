public class MathSolver {
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 5;
        double result = add(num1, num2);
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + result);
    }

    public static double add(int a, int b) {
        return a + b;
    }
}
