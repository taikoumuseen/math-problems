public class MathSolver {
    public static void main(String[] args) {
        // Example of solving basic arithmetic operations
        System.out.println("Result: 5 + 3 = " + (5 + 3));
        System.out.println("Result: 10 - 2 * 4 = " + (10 - 2 * 4));
        System.out.println("Result: 7 / 2 = " + (7 / 2));

        // Example of using arithmetic operations to solve a quadratic equation
        double x = Math.sqrt(25);
        System.out.println("Roots of the equation x^2 - 5x + 6 = 0 are: " + x);
    }
}
